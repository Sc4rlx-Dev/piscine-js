function max(a,b){
    let big

    if (a > b) {
        big = a
    } else if (b > a){
        big = b
    } else {
        return a
    }
return big
}

function min(c , d) {
    let small
    if (c < d) {
        small = c
    } else if (d < c) {
        small = d
    } else {
        return d
    }
return small
}
